/*  Las condicionales son estructuras de código que permiten
 *  comprobar si una expresión devuelve true o no, y después
 *  ejecuta un código diferente dependiendo del resultado. 
 *  La forma de condicional más común es la llamada if... else.
 */

let helado = 'chocolate';

if (helado === 'chocolate'){
    console.log('El helado es de chocolate');
} else {
    console.log('El helado no es de chocolate');
}